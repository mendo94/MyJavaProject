package TicTacToe;

public class TicTacToe {
    public static void main(String[] args) {

        char[] pos = {'X', ' ', ' ', ' ', ' ', ' ', ' ', ' ', ' '};

        System.out.println(" " + pos[6] + " | " + pos[7] + " | " + pos[8]  + " " );
        System.out.println("----+----+-----");
        System.out.println(" " + pos[3] + " | " + pos[4] + " | " + pos[5]  + " " );
        System.out.println("----+----+-----");
        System.out.println(" " + pos[0] + " | " + pos[1] + " | " + pos[2]  + " " );
    }
}
